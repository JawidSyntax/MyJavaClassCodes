package com.class02;

public class Additionsubtraction {
	public static void main(String[] args) {
//Declare 2 int variables and then we perform
		//addition, subtruction, multiplication, division
		
		int a=30;
		int b=15;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		int sum=a+b;
		int sub=a-b;
		int mult=a*b;
		int div=a/b;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);
		
		// print: the___ of 2 number is____
		
		System.out.println("the addition of 2 number is "+sum);
		System.out.println("the subtraction of 2 number is "+sub);
		System.out.println("the multiplication of 2 number is "+mult);
		System.out.println("the division of 2 number is "+div);
		
		
		
	
	}

}
